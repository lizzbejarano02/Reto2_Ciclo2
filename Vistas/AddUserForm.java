    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        //|C.C|C.E|L.M|P|Otro|
         //Valor de la lista desplegable que se ha seleccionado
        String tipoDocumento =(String)cbTipoDocumento.getSelectedItem();
        //Posicion de la lista desplegable seleccionada
        int posicionLista = cbTipoDocumento.getSelectedIndex();
        String documento = txtDocumento.getText();
        String correo = txtCorreo.getText();
        
        //Validamos que se capture correctamente la informacion
        System.out.println("nombre: " + nombre+ " " +apellidos
                           +", documento: "+tipoDocumento+ " " + documento
                           +", correo: " + correo);
        
        //Validamos que todos los campos del formulario esten diligenciados
        if(nombre.isEmpty() || apellidos.isEmpty() || documento.isEmpty() || correo.isEmpty()){
            JOptionPane.showMessageDialog(this, "Faltan campos por diligenciar", "Registro", JOptionPane.WARNING_MESSAGE);
        }else{
            String query = "INSERT INTO `empleados`(`nombreEmp`, `apellidos`, `tipodocumento`, `documento`, `correo`) "
                           + "VALUES ('"+nombre+"','"+apellidos+"','"+tipoDocumento+"','"+documento+"','"+correo+"')";
            try{
                connection= conexion.getConnection();
                st= connection.createStatement();
                //El executeUpdate se utilza para las funciones de:
                //Crear, eliminar y actualizar la informacion de los empleados
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "El usuario ha sido registrado");
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, "El usuario no ha sido registrado", "Empleados", JOptionPane.ERROR_MESSAGE);
            }
            this.dispose(); 
        }            
    }//GEN-LAST:event_btnGuardarActionPerformed

